                <h2>mutum ça sert à quoi ?</h2>
                <br />
                A consommer !
                <br /><br />
                mutum.fr est une plateforme qui permet d’utiliser les objets que nous n'avons pas et de prêter ceux qui nous servent peu.
                En créant une « bibliothèque en ligne » de nos objets, chacun a la possibilité de consommer plus, sans dépenser d’argent.
                <br /><br />
                <span class="blue">mutum ça veut dire quoi ?</span>
                <br /><br />
                Du latin Mutuum, c’était le premier contrat à entente mutuelle dans l’antiquité romaine. Ça se prononce donc Moutoum. Et non, les fondateurs ne parlent pas latin !
                <br /><br />
                <span class="blue">Qu’est-ce qu’un mutum ?</span>
                <br /><br />
                Il s’agit de l’unité de mesure de notre plateforme. Cela permet d’évaluer la valeur d’utilisation d’un objet pour rendre chaque transaction équitable et donner un intérêt aux prêteurs.
                <br /><br />
                <span class="blue">Comment est calculé la valeur de mon objet en mutum ?</span>
                <br /><br />
                En référencant un objet, vous mettez le prix de la valeur d'achat de celui ci. <br />
                A partir de cette valeur d'achat que vous référencez, mutum calcule la valeur d'utilisation de votre objet grace à un logarithme. <br />
                Vous devez aussi mettre la durée de votre prêt, mettez le en fonction d'une durée logique de prêt d'un objet, par exemple :<br />
                - une paire de ski s'emprunte 1 semaine minimum.<br />
                - Un appareil à raclette ou une perceuse s'empruntent 2 ou 3 jours...
                <br /><br />
                <span class="blue">Comment le site gagne de l'argent ?</span>
                <br /><br />
                Pas par l'inscription qui est gratuite !
                Si vous jouez le jeu et que vous prétez des objets, vous cumulerez assez de mutum pour pouvoir emprunter gratuitement.
                Par contre, si vous ne voulez pas prêter mais que vous voulez quand même emprunter, vous pouvez acheter des mutum via la page "acheter des mutum".
                <br /><br />
                Ou encore, si vous avez référencé des objets sur votre site et que vous n'avez pas assez de mutum pour emprunter la tondeuse de vos rêves, vous pouvez acheter des mutum via la page acheter des mutum.
                <br /><br />
                <span class="blue">Puis-je revendre mes mutum en trop ?</span>
                <br /><br />
                Non, je ne peux pas revendre mes mutum, ils sont considérés comme des crédits d'utilisation et non pas comme une monnaie virtuelle.
                <br /><br />
                <span class="blue">Si mon objet m'est retourné cassé, volé, détérioré ?</span>
                <br /><br />
                Les études le prouvent, les objets empruntés entre particuliers reviennent en meilleur état que ceux loués à un professionnel.
                Pour chaque objet, nous vous conseillons de mettre une garantie bancaire. Cela permet de sécuriser l’échange. L’emprunteur mettra une empreinte de carte bancaire pour le montant sélectionné qui sera prélevé de tout ou partie en cas de litige. Nous vous recommandons d’être précis dans l’état des lieux de votre fiche objet. Les coordonnées bancaires sont conservées par notre partenaire Mangopay et en aucun cas par mutum.
                <br /><br />
                <span class="blue">Comment savoir si l’objet que je veux emprunter est en bon état ?</span>
                <br /><br />
                Il s’agit d’abord d’un engagement du propriétaire lorsqu'il s’inscrit sur mutum. Ensuite l’état des lieux se doit d’être conforme à l’objet que vous allez trouver, sans quoi vous risquez de mettre une très mauvaise évaluation à l’objet et au prêteur.
                <br /><br />
                <span class="blue">mutum est un site de location finalement ?</span>
                <br /><br />
                Non, mutum est une plateforme de mise en relation entre des personnes proches géographiquement. Nous aidons ces personnes à consommer plus en valorisant les objets dont ils ne se servent pas ou peu.
                <br /><br />
                <span class="blue">Y a-t-il un système de caution ?</span>
                <br /><br />
                Le prêteur peut demander une caution à l'emprunteur pour sécuriser son transfert. La caution se fait par empreinte de carte bancaire auprès de notre partenaire Mangopay. Pour pallier les frais bancaires et seulement si la caution est débitée, cette caution sera facturée 0.27 € à la charge de l'emprunteur. Nous préférons éviter la caution par chèque, celle-ci étant peu sûre.
                <br /><br />
                <span class="blue">Puis-je annuler une réservation ?</span>
                <br /><br />
                Oui, vous pouvez annuler votre réservation à tout moment, faites cependant attention à la note que pourra vous mettre le prêteur si il n'y a pas de motif valable.
                <br /><br />
                <span class="blue">Je souhaite prolonger mon prêt ?</span>
                <br /><br />
                La demande doit être faite au propriétaire avant la fin de la première période. Vous pouvez alors lui adresser une nouvelle demande, tout simplement, sauf que l’objet est déjà chez vous.
                <br /><br />
                <span class="blue">Que se passe-t-il si je suis en retard à un rdv ?</span>
                <br /><br />
                Une fois qu’une transaction est validée, les coordonnées des deux personnes s’affichent, donc anticipez en notant le numéro de téléphone de la personne et appelez !
                <br /><br />
                <span class="blue">Est-ce-que je dois déclarer les sommes gagnées en mutum ?</span>
                <br /><br />
                Contrairement à de la location entre particuliers, vous ne gagnez rien d’autre que des mutum, vous n’avez donc pas à déclarer ces revenus.
